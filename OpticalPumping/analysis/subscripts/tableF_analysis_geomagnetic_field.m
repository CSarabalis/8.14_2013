%% Convert data into appropriate units, quantities

sweeprate = (tableF(:,8)-tableF(:,7))./tableF(:,9);
sweepstart = tableF(:,7);
peak_1_freq = tableF(:,5).*sweeprate + sweepstart;
peak_2_freq = tableF(:,6).*sweeprate + sweepstart;
instr_uncert = ones(size(tableF,1),1)*0.001;

%% Varying Y-coil current

yrange = [1:15];

y_mag_field = i2b(tableF(:,2),'y');

[x, i] = sort(y_mag_field(yrange));
y1 = peak_1_freq(yrange(i));
y2 = peak_2_freq(yrange(i));
sig=instr_uncert(yrange(i));

%% Error analysis for Y-coil

% error in determination of current
uncert_I = 0.1; % coil ammeters quote current to +-0.1mA
uncert_I_B = i2b(uncert_I,'y'); % convert current uncert to magentic field
dBdI = i2b(1,'y')^2;

% error in coil geometry (specifically radius)
R_Y = 8.9*2.54*0.01; % radius of coil in metres
uncert_R_B = 0.0075; % uncertainty in coil radius in metres (estimated)
dBdR = i2b(tableF(:,2)/R_Y,'y'); % dB/dR
dBdR = dBdR(yrange(i)); % sort them into the correct order

% total error in magnetic field
uncert_B = sqrt(uncert_I_B^2*dBdI + uncert_R_B^2*dBdR.^2);

% convert magnetic field error to frequency^2 uncertainty for coil 1
grads = zeros(size(y1,1),1);
diffs = diff(y1.^2)./diff(x);
grads(2:end-1) = (diffs(1:end-1) + diffs(2:end))/2; % generate derivative of f^2 w.r.t. b
grads(1) = diffs(1);
grads(end) = diffs(end);
sig1 = grads.*uncert_B + sig;

% convert magnetic field error to frequency^2 uncertainty for coil 2
grads = zeros(size(y2,1),1);
diffs = diff(y2.^2)./diff(x);
grads(2:end-1) = (diffs(1:end-1) + diffs(2:end))/2; % generate derivative of f^2 w.r.t. b
grads(1) = diffs(1);
grads(end) = diffs(end);
sig2 = grads.*uncert_B + sig;


